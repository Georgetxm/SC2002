package types;

/**
 * Represents the Role ENUM
 * Used to represent the role of a user in a camp
 * 
 * @author Teo Xuan Ming
 * @version 1.1
 * @since 2021-11-24
 */

public enum Role {
	/**
	 * The role of a student who is attending the camp
	 */
	ATTENDEE,
	/**
	 * The role of a student who is a committee member in the camp
	 */
	COMMITTEE,
	/**
	 * The role of a staff who is in charge of the camp
	 */
	STAFF
}
